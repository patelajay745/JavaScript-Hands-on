// Number data type
let num = 42;

// String data type
let str = "Hello, world!";

// Boolean data type
let bool = true;

// Undefined data type
let undef;

// Null data type
let nul = null;

// Object data type
let obj = {
  name: "John",
  age: 30,
  isStudent: false
};

// Array data type
let arr = [1, 2, 3, "apple", true];

// Function data type
function greet(name) {
  return "Hello, " + name + "!";
}

// Symbol data type
let sym = Symbol("foo");

console.log("Number:", num);
console.log("String:", str);
console.log("Boolean:", bool);
console.log("Undefined:", undef);
console.log("Null:", nul);
console.log("Object:", obj);
console.log("Array:", arr);
console.log("Function:", greet("Alice"));
console.log("Symbol:", sym);
